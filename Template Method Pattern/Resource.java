	public static List<Course> getCourses() {
		var rndm = new Random();
		var list = new ArrayList<Course>();
		for(var glists : genedCourses.values())
			if(rndm.nextBoolean())
				list.add(glists.get(rndm.nextInt(glists.size())));
		list.sort(Comparator.comparingInt(course -> course.geneds().size()));
		return list;
	}

	public static String languageOf(Course crs) {
		String returnVal = "";
		String title = crs.number();
		switch (title.substring(0,3)) {
		case "ARA": return "Arabic";
		case "CHI": return "Chinese";
		case "FRE": return "French";
		case "GER": return "German";
		case "HEB": return "Hebrew";
		case "ITA": return "Italian";
		case "JPN": return "Japanese";
		case "KOR": return "Korean";
		case "LAT": return "Latin";
		case "RUS": return "Russian";
		case "SPA": return "Spanish";
		case "TUR": return "Turkish";
		case "YID": return "Yiddish";
		}
		if (title.startsWith("COLI 180")) return "German";
		if (title.startsWith("JUST 180")) return "Yiddish";
		if (title.startsWith("LING 2")) return "American Sign Language";
		if(title.equals("LING 498-8")) return "Linguistics Seminar";
		if (title.startsWith("LACS 344")) return "Spanish";		
		return returnVal;
	}

